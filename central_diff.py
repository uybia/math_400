from math import *

def g_x(x):
    return (e**((-x**2)/2))
def g_xx(x):
    return ((e**((-x**2)/2))*(-x))

def central_diff(x, h):
    return ((g_x(x+h) - g_x(x-h))/(2*h)) 

actual = g_xx(1.4)
print("g'(1.4) = %1.20f" %(actual))
print("-----------------------------------------------------------------------------")
print("|         h       |           g(1.4)           |            error            |")
print("-----------------------------------------------------------------------------")
for x in range(1,21):
    val = central_diff(1.4,(10.**(-x)))
    print("|   %.9f   |   %2.20f   |   %.20f    |" %((10.**(-x)), val, abs((actual-val))))
          
print("-----------------------------------------------------------------------------")
